export function converterEnum(en: string) {
    switch(en) {
        case 'SEGUNDA_FEIRA': {
            return 'SEGUNDA FEIRA';
        }
        case 'TERCA_FEIRA': {
            return 'TERÇA FEIRA';
        }
        case 'QUARTA_FEIRA': {
            return 'QUARTA FEIRA';
        }
        case 'QUINTA_FEIRA': {
            return 'QUINTA FEIRA';
        }
        case 'SEXTA_FEIRA': {
            return 'SEXTA FEIRA';
        }
        case 'SABADO': {
            return 'SÁBADO';
        }
    }
}